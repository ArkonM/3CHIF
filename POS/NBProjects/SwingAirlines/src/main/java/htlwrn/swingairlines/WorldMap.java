    public void setPicture() {
        try {
            img = ImageIO.read(new File("Earthmap.jpg"));
        } catch (IOException ex) {
            Logger.getLogger(WorldMap.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    @Override
    public void paint(Graphics g) {
        super.paint(g);
        Graphics2D g2 = (Graphics2D) g;
        int w = this.getWidth();
        int h = this.getHeight();
        g2.drawImage(img, 0, 0, w, h, 0, 0, img.getWidth(), img.getHeight(), null);
        drawInPicture(g);
    }
    
    public void drawInPicture(Graphics g) {
        
        if(listExists()) {
            
            int[][] coordinates = new int[routeList.size()][2];
            for(int i = 0; i < routeList.size(); i++) {
                
                g.setColor(Color.red);

                int posX = (int)(obj.getAirportFromIata(routeList.get(i)).getLongitude() + 180) * this.getWidth() / 360; // -5 -> Punkt soll in der Mitte vom Kreis sein, Kreisl√§nge 10
                int posY = (int)(90 - obj.getAirportFromIata(routeList.get(i)).getLatitude()) * this.getHeight() / 180;
                
                coordinates[i][0] = posX;
                coordinates[i][1] = posY;
                
                g.drawOval(posX -  5, posY - 5, 10, 10);
               
                g.setColor(Color.white);
                g.drawChars(obj.getAirportFromIata(routeList.get(i)).getIata().toCharArray(), 0, start.getIata().toCharArray().length
                    , posX + 10, posY - 3);
                
            }
            
            for(int i = 1; i < coordinates.length; i++) {
                g.drawLine(coordinates[i - 1][0], coordinates[i - 1][1], coordinates[i][0], coordinates[i][1]);
            }
        }
    }
    
    private boolean listExists() {
        if(routeList == null) return false;
        
        for(String iata : routeList) {
            if(iata == null) return false;
        }
        return true;
    }
    
